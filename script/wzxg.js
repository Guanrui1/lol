// JavaScript Document
function span(x,y){
	var spx=document.getElementById(x);
	var sp=document.getElementById(y);
	
	sp.style.border="2px solid #e6bf6b";
	
	spx.style.color="#e6bf6b";
	}
	function spanout(x,y){
	var spx=document.getElementById(x);
	var sp=document.getElementById(y);
	
	sp.style.border="0px solid #e6bf6b ";
	
	spx.style.color="#FFF";
	}